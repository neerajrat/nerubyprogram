def parametar(var , var2)
    sum = var + var2
    return sum

end
def parametar2(var , var2)
    sum = var - var2
    return sum

end
def parametar3(var , var2)
    sum = var / var2
    return sum

end
def parametar4(var , var2)
    sum = var * var2
    return sum

end

